package src.ComparatorImplementationUsingHashmap;

/**
 * @author Tara P
 */
public class RefinementOptions {

    private int refinementCount;

    private String multiSelect;

    private Boolean selectionFlag;

    private String label;

    private String filter;

    private Boolean displayFlag;

    public int getRefinementCount() {
        return refinementCount;
    }

    public void setRefinementCount(int refinementCount) {
        this.refinementCount = refinementCount;
    }

    public String getMultiSelect() {
        return multiSelect;
    }

    public void setMultiSelect(String multiSelect) {
        this.multiSelect = multiSelect;
    }

    public Boolean getSelectionFlag() {
        return selectionFlag;
    }

    public void setSelectionFlag(Boolean selectionFlag) {
        this.selectionFlag = selectionFlag;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getFilter() {
        return filter;
    }

    public void setFilter(String filter) {
        this.filter = filter;
    }

    public Boolean getDisplayFlag() {
        return displayFlag;
    }

    public void setDisplayFlag(Boolean displayFlag) {
        this.displayFlag = displayFlag;
    }
}
